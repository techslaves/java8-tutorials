package operators;

public class ArithmaticDemo {
    /*
    + Additive operator
    - Subtraction
    * multiplication
    / Division
    % Remainder
     */

    public static void main(String[] args) {
        int result = 2+3;
        System.out.println("1+2 " + result);

        int result2 = result-1;
        System.out.println("1+2  " + result2);
        System.out.println("Hello " + "1 " + "US");

        int result3 = result2*2;
        System.out.println("Mulitplication Answer = " + result3);

        int result4 = result3/1;
        System.out.println("Division = " + result4);

        int result5 = result4%3;
        System.out.println("Remainder = " + result5);


    }

}
